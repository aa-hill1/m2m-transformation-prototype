package org.example.app.transformModel.generalComps;

/**
 * Enum representing the possible types of {@code NameOnlyComponent} and {@code ContextComponent} instances.
 */
public enum SimpleCompType {
    IMPORTS, // ContextComponent class only
    RECORD, // ContextComponent class only
    ENUMERATION, // ContextComponent class only
    RCINTERFACE, // ContextComponent class only
    FUNCTION, // ContextComponent class only
    TYPE_DEC, // NameOnlyComponent class only
    RCPACKAGE // NameOnlyComponent class only
}
